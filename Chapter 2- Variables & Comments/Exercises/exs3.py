#Then print the name using each of the three stripping functions, lstrip(), rstrip(), and strip().

name = "\jack\n"

print("Unmodified:")
print(name)

print("\nUsing lstrip():")
print(name.lstrip())

print("\nUsing rstrip():")
print(name.rstrip())

print("\nUsing strip():")
print(name.strip())